        private int brightness = 15;
        public int Brightness {
            get => this.brightness;
            set {
                this.brightness = value;

                if (this.brightness < 0 || this.brightness > 15) { throw new Exception("brightness must be in range [0.15]"); }

                var cmd = (byte)(HT16K33_CMD_BRIGHTNESS | this.brightness);

                this.dueController.I2c.Write(this.SlaveAddress, new byte[] { cmd });
            }
        }
        const byte HT16K33_CMD_BRIGHTNESS = 0xE0;
        const byte HT16K33_BLINK_CMD = 0x80;
        const byte HT16K33_BLINK_DISPLAYON = 0x01;
        const byte HT16K33_OSCILLATOR_ON = 0x21;

        public HT16K33Controller(DUEController dueController, byte slaveAddress = 0x70, int row = 8, int column = 8, int rotation = 0) {
            this.dueController = dueController;
            this.SlaveAddress = slaveAddress;
            this.Column = column;
            this.Row = row;
            this.Rotation = rotation;
            this.data = new ushort[row];

            var cmd = HT16K33_OSCILLATOR_ON;

            this.dueController.I2c.Write(this.SlaveAddress, new byte[] { cmd });

            cmd = HT16K33_BLINK_CMD | HT16K33_BLINK_DISPLAYON;

            this.dueController.I2c.Write(this.SlaveAddress, new byte[] { cmd });


        }     

        public void SetPixel(uint color, int x, int y) {
            if (x < 0 || x > this.Column) {
                throw new Exception(string.Format("x must be in range [0,{0}]", this.Column));
            }

            if (y < 0 || y > this.Row) {
                throw new Exception(string.Format("y must be in range [0,{0}]", this.Row));
            }

            switch (this.Rotation) {
                case 1:
                    Swap(ref x, ref y);
                    x = this.Column - x - 1;
                    break;

                case 2:
                    x = this.Column - x - 1;
                    y = this.Row - y - 1;
                    break;

                case 3:
                    Swap(ref x, ref y);
                    y = this.Row - y - 1;
                    break;
            }

            x += 7;
            x %= 8;

            if (color != 0) {
                this.data[y] |= (ushort)(1 << x);
            }
            else {
                this.data[y] &= (ushort)~(1 << x);
            }
        }

        public void Clear() => Array.Clear(this.data);
        public void Show() {
            var dataWrite = new byte[this.data.Length * 2 + 1];

            dataWrite[0] = 0;// cmd

            for (var i = 0; i < this.data.Length; i++) {
                dataWrite[1 + i * 2 + 0] = (byte)(this.data[i] & 0xFF);
                dataWrite[1 + i * 2 + 1] = (byte)((this.data[i] << 8) & 0xFF);
            }
            this.dueController.I2c.Write(this.SlaveAddress, dataWrite);
        }

        private static void Swap(ref int x, ref int y) {
            var t = x;
            x = y;
            y = t;
        }

        public void DrawCharacter(char character, uint color, int x, int y) {
            var index = 5 * (character - 32);
            for (var horizontalFontSize = 0; horizontalFontSize < 5; horizontalFontSize++) {
                var sx = x + horizontalFontSize;
                var fontRow = this.mono8x5[index + horizontalFontSize];
                for (var verticleFontSize = 0; verticleFontSize < 8; verticleFontSize++) {
                    if ((fontRow & (1 << verticleFontSize)) != 0) this.SetPixel(color, sx, y + verticleFontSize);
                }
            }

        }


