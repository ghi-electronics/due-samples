        private void WriteByte(byte register, byte value) {
            var temp = new byte[2];
            temp[0] = register;
            temp[1] = value;            
            this.dueController.I2c.Write(this.SlaveAddress, temp);
        }
        private void WriteBuffer(byte register, byte[] value) {
            var temp = new byte[value.Length + 1];

            temp[0] = register;
            for (var x = 0; x < value.Length; x++) {
                temp[x + 1] = value[x];
            }            
            this.dueController.I2c.Write(this.SlaveAddress, temp);

        }

        public ScrollBitController(DUELinkController dueController, byte slaveAddress = 0x74) {
            this.dueController = dueController;
            this.SlaveAddress = slaveAddress;

            this.Clear();
            this.WriteByte( REG_BANK, BANK_CONFIG);
            
            Thread.Sleep(1);
            this.WriteByte( REG_SHUTDOWN, 0);
            Thread.Sleep(1);

            this.WriteByte( REG_SHUTDOWN, 1);
            Thread.Sleep(1);


            this.WriteByte( REG_MODE, 0);
            this.WriteByte( REG_AUDIOSYNC, 0);

            var enable = new byte[17];
            Array.Fill<byte>(enable, 255);
            this.WriteByte( REG_BANK, 0);
            this.WriteBuffer( 0, enable);
            this.WriteByte( REG_BANK, 1);
            this.WriteBuffer( 0, enable);
        }
        public void Show() {
            var corrected_buf = new byte[144];

            for (var x = 0; x < this.buf.Length; x++) {
                corrected_buf[x] = this.CorrectGamma(this.buf[x]);
            }

            this.WriteByte( REG_BANK, this.frame);
            this.WriteBuffer(REG_COLOR, corrected_buf);
            this.WriteByte( REG_BANK, BANK_CONFIG);
            this.WriteByte( REG_FRAME, this.frame);

            this.frame = (byte)(this.frame == 0 ? 1 : 0);
        }
        public void Clear() => Array.Clear(this.buf);
        public void SetPixel(int col, int row, uint brightness = 128) {

            if (col < 0 || row < 0 || col >= COLS || row >= ROWS) { return; }

            if (this.UpSideDown) {
                col = (COLS - 1) - col; row = (ROWS - 1) - row;
            }
            var index = this.PixelAddr(col, row);
            this.buf[index] = (byte)Math.Clamp(brightness, 0, 255);
        }

        private uint PixelAddr(int col, int row) {
            row = 7 - (row + 1);

            if (col > 8) {
                col = col - 8;
                row = 6 - (row + 8);
            }
            else {
                col = 8 - col;
            }

            return (uint)((col * 16) + row);
        }
